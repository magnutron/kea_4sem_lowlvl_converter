int kilograms_to_ounces(int kilograms) {
    return kilograms * 35.274;
}

int kilograms_to_pounds(int kilograms) {
    return kilograms * 2.20462;
}

int ounces_to_kilograms(int ounces) {
    return ounces / 35.274;
}

int ounces_to_pounds(int ounces) {
    return ounces / 16;
}

int pounds_to_kilograms(int pounds) {
    return pounds * 0.453592;
}

int pounds_to_ounces(int pounds) {
    return pounds * 16;
}